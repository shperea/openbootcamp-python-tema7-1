def suma(x, y):

    """
    Suma dos numeros, x mas y
    """

    return x + y

def resta(x, y):

    """
    Resta dos numeros, x menos y
    """

    return x - y

def multiplicacion(x, y):

    """
    Multiplicacion de dos numeros, x e y
    """

    return x * y

def division(x, y):

    """
    Division de dos numeros, x entre y
    """

    return x / y